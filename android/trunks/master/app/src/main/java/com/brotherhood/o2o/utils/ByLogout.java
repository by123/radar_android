package com.brotherhood.o2o.utils;


import android.util.Log;

public class ByLogout {
    public static void out(String content) {
        Log.i("by", content);
    }

    public static void out2(String content) {
        Log.i("by2", content);
    }

    public static void out3(String content) {
        Log.i("by3", content);
    }

    public static void out(String tag, String content) {
        Log.i(tag, content);
    }

}
